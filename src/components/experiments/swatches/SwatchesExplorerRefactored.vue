<script setup>
import { ref, onMounted, onUnmounted } from 'vue'
import { useRouter } from 'vue-router'

// Components
import Header from './components/shared/Header.vue'
import ColorCarousel from './components/carousel/ColorCarousel.vue'
import GridControls from './components/grid/GridControls.vue'
import PaletteGrid from './components/grid/PaletteGrid.vue'

// Composables
import { useColorData } from './composables/useColorData.js'

const router = useRouter()

// Use color data composable
const { palette, allColors } = useColorData()

// Grid size state shared between components
const gridSize = ref(4)

// Handle navigation
const handleClose = () => {
  document.body.style.overflow = ''
  router.back()
}

// Handle grid size changes from GridControls
const handleGridSizeChange = (newSize) => {
  gridSize.value = newSize
}

// Setup and cleanup
onMounted(() => {
  document.body.style.overflow = 'hidden'
})

onUnmounted(() => {
  document.body.style.overflow = ''
})
</script>

<template>
  <div class="swatches-explorer">
    <Header 
      :title="palette.name"
      @close="handleClose"
    />
    
    <ColorCarousel
      :colors="allColors"
      :palette-name="palette.name"
    />
    
    <GridControls
      :grid-size="gridSize"
      @size-change="handleGridSizeChange"
    />
    
    <div class="main-content">
      <PaletteGrid
        :colors="allColors"
        :initial-grid-size="gridSize"
        :grid-size="gridSize"
        @grid-size-change="handleGridSizeChange"
      />
    </div>

    <div class="footer">
      Generated by Swatch and Palette Maker by Katie O'Connor
    </div>
  </div>
</template>

